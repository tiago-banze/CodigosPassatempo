print('Programa de media de um aluno.\n\n')
nome=input('IMPRIMA O SEU NOME:\t')
nota1=float(input('\nIMPRIMA A PRIMEIRA NOTA:\t'))
nota2=float(input('\nIMPRIMA A SEGUNDA NOTA:\t'))
media=(nota1+nota2)/2
print(f'\n\t{nome} tua media final é {media}')
